#include<stdio.h>
#include<stdlib.h>

/**
 * main - entry point, prints the first fifty fibonnanci numbers
 *
 *
 * Return: 0 (success)
 */
int main(void)
{
    int n;
    int i[] = {n, n++};
    for (n = 0; n < 100; n++)
    {
        printf("%d, ", i, i);
        return 0;
    }
    printf("\n");
}